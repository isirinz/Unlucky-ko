package com.trialmobile.unlucky.map;

/**
 * Maps can have special weather effects
 *
 * @author Ming Li
 */
public enum WeatherType {

    // no weather
    NORMAL,
    // rain
    RAIN,
    // pouring rain
    HEAVY_RAIN,
    // thunderstorm (heavy rain with flashes of light)
    THUNDERSTORM,
    // snow
    SNOW,
    // blizzard (faster and heavier snow)
    BLIZZARD

}
